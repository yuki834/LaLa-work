package sample;

public class Logi {

public static double plus(double x, double y) {
		
		return (x + y);
	}
	
	public static double hiku(double x, double y) {
		
		return (x - y);
	}
	
	public static double kakeru(double x, double y) {
		
		return (x * y);
	}
	
	public static double waru(double x, double y) {
		
		return (x / y);
	}

	
}
