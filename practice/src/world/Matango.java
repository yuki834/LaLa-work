package world;

public class Matango {

	private String name = "matango";
	private int hp;
	private int mp;
	
	public int getHp() {
		return hp;
	}
	public int getMp() {
		return mp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public void setMp(int mp) {
		this.mp = mp;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
